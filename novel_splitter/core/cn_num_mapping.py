def cn_num_to_arabic(cn_num: str) -> int:
    """
    中文数字转阿拉伯数字
    
    Args:
        cn_num: 中文数字（如：'一', '十一', '一百二十三', '三千五百', '五万三千'）
            
    Returns:
        int: 阿拉伯数字
    """
    # 单个或两个数字（1-99）
    if len(cn_num) <= 2:
        return CN_NUM_TO_ARABIC.get(cn_num, 0)
    
    # 处理大数字（三位及以下）
    if '千' in cn_num:
        thousand_pos = cn_num.index('千')
        thousand = CN_NUM_TO_ARABIC[cn_num[:thousand_pos]]
        after_thousand = cn_num[thousand_pos+1:]
        return thousand * 1000 + cn_num_to_arabic(after_thousand)
    
    if '百' in cn_num:
        hundred_pos = cn_num.index('百')
        hundred = CN_NUM_TO_ARABIC[cn_num[:hundred_pos]]
        after_hundred = cn_num[hundred_pos+1:]
        return hundred * 100 + cn_num_to_arabic(after_hundred)
    
    if '万' in cn_num:
        ten_thousand_pos = cn_num.index('万')
        ten_thousand = CN_NUM_TO_ARABIC[cn_num[:ten_thousand_pos]]
        after_ten_thousand = cn_num[ten_thousand_pos+1:]
        return ten_thousand * 10000 + cn_num_to_arabic(after_ten_thousand)
    
    # 处理普通数字（3-4位）
    result = 0
    for char in cn_num:
        if char in CN_NUM_TO_ARABIC:
            if char in ['百', '千', '万']:
                result = result * CN_NUM_TO_ARABIC[char]
            else:
                result = result * 10 + CN_NUM_TO_ARABIC[char]
    
    return result